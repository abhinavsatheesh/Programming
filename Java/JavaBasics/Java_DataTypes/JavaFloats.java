package Java_DataTypes;

public class JavaFloats {
	public static void main(String[] args) {
		float f1 = 0.5f;
		System.out.println("Your number " + f1 + " is of type java.lang.Float");
		float f2 = 1.5f;
		System.out.println("Your number " + f2 + " is of type java.lang.Float");
		float f3 = 2.5f;
		System.out.println("Your number " + f3 + " is of type java.lang.Float");
		float f4 = 3.5f;
		System.out.println("Your number " + f4 + " is of type java.lang.Float");
	}
}